drop policy if exists "manager manages events" on public.events;
create policy "manager manages events" on public.events
for all to authenticated using (public.is_nostra_manager()) with check (public.is_nostra_manager());

-- Les membres peuvent déposer et consulter leurs demandes d'homologation.
drop policy if exists "users create homologation requests" on public.homologation_requests;
create policy "users create homologation requests" on public.homologation_requests
for insert to authenticated with check (user_id = auth.uid());

drop policy if exists "users read homologation requests" on public.homologation_requests;
create policy "users read homologation requests" on public.homologation_requests
for select to authenticated using (user_id = auth.uid() or public.is_nostra_manager());

drop policy if exists "manager updates homologation requests" on public.homologation_requests;
create policy "manager updates homologation requests" on public.homologation_requests
for update to authenticated using (public.is_nostra_manager()) with check (public.is_nostra_manager());

drop policy if exists "manager deletes homologation requests" on public.homologation_requests;
create policy "manager deletes homologation requests" on public.homologation_requests
for delete to authenticated using (public.is_nostra_manager());

