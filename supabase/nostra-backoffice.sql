-- Lecture des informations publiques du site par les membres connectés.
drop policy if exists "circuit settings readable" on public.circuit_settings;
create policy "circuit settings readable" on public.circuit_settings
for select to authenticated using (true);

drop policy if exists "events readable" on public.events;
create policy "events readable" on public.events
for select to authenticated using (status <> 'draft' or public.is_nostra_manager());

-- Gestion complète réservée au gérant.
drop policy if exists "manager manages circuit settings" on public.circuit_settings;
create policy "manager manages circuit settings" on public.circuit_settings
for all to authenticated using (public.is_nostra_manager()) with check (public.is_nostra_manager());

drop policy if exists "manager manages inventory" on public.inventory_items;
create policy "manager manages inventory" on public.inventory_items
for all to authenticated using (public.is_nostra_manager()) with check (public.is_nostra_manager());

