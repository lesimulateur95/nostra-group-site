-- NOSTRA GROUP — extension du dashboard
-- Ce script conserve les contenus déjà enregistrés.

create or replace function public.is_nostra_manager()
returns boolean
language sql
stable
as $$
  select coalesce(
    auth.jwt() -> 'user_metadata' ->> 'provider_id',
    auth.jwt() -> 'user_metadata' ->> 'discord_id',
    auth.jwt() -> 'user_metadata' ->> 'sub'
  ) = '331843410962939908';
$$;

grant execute on function public.is_nostra_manager() to authenticated;

create table if not exists public.circuit_settings (
  id integer primary key default 1 check (id = 1),
  status text not null default 'open',
  label text not null default 'Circuit ouvert',
  message text not null default 'Le Nostra Circuit est actuellement ouvert.',
  updated_at timestamptz not null default now(),
  updated_by uuid references auth.users(id) on delete set null
);

insert into public.circuit_settings (id)
values (1)
on conflict (id) do nothing;

create table if not exists public.inventory_items (
  id bigint generated by default as identity primary key,
  name text not null,
  category text not null default 'Général',
  sku text,
  quantity integer not null default 0 check (quantity >= 0),
  minimum_quantity integer not null default 0 check (minimum_quantity >= 0),
  unit_price numeric(12,2) not null default 0 check (unit_price >= 0),
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  updated_by uuid references auth.users(id) on delete set null
);

create table if not exists public.accounting_entries (
  id bigint generated by default as identity primary key,
  entry_date date not null default current_date,
  entry_type text not null check (entry_type in ('income', 'expense')),
  category text not null default 'Général',
  label text not null,
  amount numeric(12,2) not null check (amount >= 0),
  notes text,
  created_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null
);

create table if not exists public.events (
  id bigint generated by default as identity primary key,
  title text not null,
  description text not null default '',
  location text not null default '',
  starts_at timestamptz not null,
  ends_at timestamptz,
  status text not null default 'draft' check (status in ('draft', 'published', 'cancelled', 'completed')),
  registration_open boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null
);

create table if not exists public.homologation_requests (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  request_type text not null check (request_type in ('vehicle', 'team')),
  applicant_name text not null,
  status text not null default 'pending' check (status in ('pending', 'reviewing', 'approved', 'rejected')),
  payload jsonb not null default '{}'::jsonb,
  admin_note text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.orders (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  order_number text not null unique,
  status text not null default 'pending',
  total numeric(12,2) not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.invoices (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  invoice_number text not null unique,
  status text not null default 'issued',
  amount numeric(12,2) not null default 0,
  issued_at timestamptz not null default now(),
  download_url text
);

create table if not exists public.loyalty_profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  tier text not null default 'Aucun statut',
  purchases_count integer not null default 0,
  discount_percent numeric(5,2) not null default 0,
  updated_at timestamptz not null default now()
);

create table if not exists public.cart_items (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  item_name text not null,
  quantity integer not null default 1 check (quantity > 0),
  unit_price numeric(12,2) not null default 0,
  image_url text,
  created_at timestamptz not null default now()
);

