create or replace function public.nostra_bingo_is_manager()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    coalesce(public.nostra_has_role('manager'), false)
    or coalesce(public.nostra_jwt_discord_id(), '') = '331843410962939908'
    or exists (
      select 1
      from public.member_profiles profile
      where profile.user_id = auth.uid()
        and (
          lower(coalesce(profile.role, '')) = 'manager'
          or 'manager' = any(coalesce(profile.roles, array[]::text[]))
          or profile.discord_id = '331843410962939908'
        )
    );
$$;

revoke all on function public.nostra_bingo_is_manager() from public;
grant execute on function public.nostra_bingo_is_manager() to authenticated;

