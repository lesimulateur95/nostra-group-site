create or replace function public.nostra_bingo_generate_card()
returns integer[]
language plpgsql
volatile
security definer
set search_path = public
as $$
declare
  v_b integer[];
  v_i integer[];
  v_n integer[];
  v_g integer[];
  v_o integer[];
begin
  -- Carton Bingo 1 à 99 :
  -- B = 1-20, I = 21-40, N = 41-60, G = 61-80, O = 81-99.
  select array_agg(candidate order by candidate) into v_b
  from (
    select candidate
    from generate_series(1, 20) as candidates(candidate)
    order by random()
    limit 5
  ) picked;

  select array_agg(candidate order by candidate) into v_i
  from (
    select candidate
    from generate_series(21, 40) as candidates(candidate)
    order by random()
    limit 5
  ) picked;

  select array_agg(candidate order by candidate) into v_n
  from (
    select candidate
    from generate_series(41, 60) as candidates(candidate)
    order by random()
    limit 4
  ) picked;

  select array_agg(candidate order by candidate) into v_g
  from (
    select candidate
    from generate_series(61, 80) as candidates(candidate)
    order by random()
    limit 5
  ) picked;

  select array_agg(candidate order by candidate) into v_o
  from (
    select candidate
    from generate_series(81, 99) as candidates(candidate)
    order by random()
    limit 5
  ) picked;

  return array[
    v_b[1], v_i[1], v_n[1], v_g[1], v_o[1],
    v_b[2], v_i[2], v_n[2], v_g[2], v_o[2],
    v_b[3], v_i[3], 0,      v_g[3], v_o[3],
    v_b[4], v_i[4], v_n[3], v_g[4], v_o[4],
    v_b[5], v_i[5], v_n[4], v_g[5], v_o[5]
  ];
end;
$$;

create or replace function public.nostra_bingo_completed_lines(
  p_numbers integer[],
  p_drawn integer[]
)
returns integer
language plpgsql
immutable
as $$
declare
  v_row integer;
  v_col integer;
  v_index integer;
  v_value integer;
  v_complete boolean;
  v_lines integer := 0;
begin
  if cardinality(p_numbers) <> 25 then return 0; end if;

  for v_row in 0..4 loop
    v_complete := true;
    for v_col in 0..4 loop
      v_index := (v_row * 5) + v_col + 1;
      v_value := p_numbers[v_index];
      if v_value <> 0 and not (v_value = any(coalesce(p_drawn, array[]::integer[]))) then
        v_complete := false;
        exit;
      end if;
    end loop;
    if v_complete then v_lines := v_lines + 1; end if;
  end loop;

  return v_lines;
end;
$$;

create or replace function public.nostra_bingo_is_full_card(
  p_numbers integer[],
  p_drawn integer[]
)
returns boolean
language sql
immutable
as $$
  select not exists (
    select 1
    from unnest(coalesce(p_numbers, array[]::integer[])) value
    where value <> 0
      and not (value = any(coalesce(p_drawn, array[]::integer[])))
  );
$$;

create or replace function public.nostra_bingo_card_matches(
  p_numbers integer[],
  p_drawn integer[],
  p_phase text
)
returns boolean
language sql
immutable
as $$
  select case p_phase
    when 'one_line' then public.nostra_bingo_completed_lines(p_numbers, p_drawn) >= 1
    when 'two_lines' then public.nostra_bingo_completed_lines(p_numbers, p_drawn) >= 2
    when 'three_lines' then public.nostra_bingo_completed_lines(p_numbers, p_drawn) >= 3
    when 'four_lines' then public.nostra_bingo_completed_lines(p_numbers, p_drawn) >= 4
    when 'full_card' then public.nostra_bingo_is_full_card(p_numbers, p_drawn)
    else false
  end;
$$;

create or replace function public.nostra_bingo_register_winners(
  p_round_id bigint,
  p_trigger_ball integer default null
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_phase text;
  v_drawn integer[];
  v_count integer := 0;
begin
  select phase into v_phase
  from public.bingo_rounds
  where id = p_round_id;

  select coalesce(array_agg(ball_number order by draw_order), array[]::integer[])
    into v_drawn
  from public.bingo_draws
  where round_id = p_round_id;

  insert into public.bingo_winners (
    round_id,
    card_id,
    phase,
    card_number,
    customer_name,
    trigger_ball
  )
  select
    card.round_id,
    card.id,
    v_phase,
    card.card_number,
    card.customer_name,
    p_trigger_ball
  from public.bingo_cards card
  where card.round_id = p_round_id
    and public.nostra_bingo_card_matches(card.numbers, v_drawn, v_phase)
  on conflict (round_id, phase, card_id) do nothing;

  get diagnostics v_count = row_count;
  return v_count;
end;
$$;

create or replace function public.add_bingo_to_cart(
  p_customer_name text,
  p_quantity integer
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_round public.bingo_rounds%rowtype;
begin
  if v_user_id is null then raise exception 'not_authenticated'; end if;
  if length(trim(coalesce(p_customer_name, ''))) < 3 then raise exception 'invalid_name'; end if;
  if p_quantity < 1 or p_quantity > 20 then raise exception 'invalid_quantity'; end if;

  select * into v_round
  from public.bingo_rounds
  where archived_at is null
  limit 1;

  if v_round.id is null then raise exception 'bingo_setup_missing'; end if;
  if v_round.status <> 'open' then raise exception 'round_closed'; end if;

  insert into public.bingo_cart_items (
    user_id, round_id, customer_name, quantity, unit_price, updated_at
  ) values (
    v_user_id, v_round.id, trim(p_customer_name), p_quantity, v_round.card_price, now()
  )
  on conflict (user_id, round_id) do update
  set customer_name = excluded.customer_name,
      quantity = excluded.quantity,
      unit_price = excluded.unit_price,
      updated_at = now();

  return jsonb_build_object(
    'quantity', p_quantity,
    'unit_price', v_round.card_price,
    'total', v_round.card_price * p_quantity
  );
end;
$$;

create or replace function public.checkout_bingo_cart()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_round public.bingo_rounds%rowtype;
  v_cart public.bingo_cart_items%rowtype;
  v_purchase_id bigint;
  v_order_number text;
  v_next_card integer;
  v_index integer;
  v_numbers integer[];
begin
  if v_user_id is null then raise exception 'not_authenticated'; end if;

  select * into v_round
  from public.bingo_rounds
  where archived_at is null
  limit 1
  for update;

  if v_round.id is null then raise exception 'bingo_setup_missing'; end if;
  if v_round.status <> 'open' then raise exception 'round_closed'; end if;

  select * into v_cart
  from public.bingo_cart_items
  where user_id = v_user_id
    and round_id = v_round.id
  for update;

  if v_cart.id is null then raise exception 'empty_cart'; end if;

  v_order_number := 'BINGO-' || to_char(clock_timestamp(), 'YYYYMMDDHH24MISSMS') || '-' || upper(substr(md5(random()::text), 1, 5));

  insert into public.bingo_purchases (
    round_id, user_id, order_number, customer_name, quantity, unit_price, total
  ) values (
    v_round.id,
    v_user_id,
    v_order_number,
    v_cart.customer_name,
    v_cart.quantity,
    v_cart.unit_price,
    v_cart.quantity * v_cart.unit_price
  ) returning id into v_purchase_id;

  select coalesce(max(card_number), 0) + 1
    into v_next_card
  from public.bingo_cards
  where round_id = v_round.id;

  for v_index in 1..v_cart.quantity loop
    v_numbers := public.nostra_bingo_generate_card();

    insert into public.bingo_cards (
      round_id, purchase_id, user_id, customer_name, card_number, numbers
    ) values (
      v_round.id,
      v_purchase_id,
      v_user_id,
      v_cart.customer_name,
      v_next_card + v_index - 1,
      v_numbers
    );
  end loop;

  delete from public.bingo_cart_items where id = v_cart.id;

  return jsonb_build_object(
    'order_number', v_order_number,
    'cards_count', v_cart.quantity,
    'total', v_cart.quantity * v_cart.unit_price
  );
end;
$$;

create or replace function public.update_bingo_settings(
  p_title text,
  p_card_price numeric,
  p_status text
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.nostra_bingo_is_manager() then raise exception 'manager_required'; end if;
  if length(trim(coalesce(p_title, ''))) < 2 then raise exception 'invalid_title'; end if;
  if p_card_price < 0 then raise exception 'invalid_price'; end if;
  if p_status not in ('open','closed') then raise exception 'invalid_status'; end if;

  update public.bingo_rounds
  set title = trim(p_title),
      card_price = p_card_price,
      status = p_status,
      updated_at = now()
  where archived_at is null;
end;
$$;

create or replace function public.set_bingo_phase(p_phase text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_round_id bigint;
  v_winners integer;
begin
  if not public.nostra_bingo_is_manager() then raise exception 'manager_required'; end if;
  if p_phase not in ('one_line','two_lines','three_lines','four_lines','full_card') then
    raise exception 'invalid_phase';
  end if;

  update public.bingo_rounds
  set phase = p_phase,
      status = case when status = 'completed' then 'playing' else status end,
      updated_at = now()
  where archived_at is null
  returning id into v_round_id;

  if v_round_id is null then raise exception 'bingo_setup_missing'; end if;
  v_winners := public.nostra_bingo_register_winners(v_round_id, null);

  return jsonb_build_object('phase', p_phase, 'new_winners', v_winners);
end;
$$;

create or replace function public.draw_bingo_number()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_round public.bingo_rounds%rowtype;
  v_ball integer;
  v_order integer;
  v_new_winners integer;
  v_winners jsonb;
begin
  if not public.nostra_bingo_is_manager() then raise exception 'manager_required'; end if;

  select * into v_round
  from public.bingo_rounds
  where archived_at is null
  limit 1
  for update;

  if v_round.id is null then raise exception 'bingo_setup_missing'; end if;

  if v_round.phase = 'full_card' and exists (
    select 1 from public.bingo_winners
    where round_id = v_round.id and phase = 'full_card'
  ) then
    raise exception 'full_card_complete';
  end if;

  select candidate into v_ball
  from generate_series(1, 99) as candidates(candidate)
  where not exists (
    select 1 from public.bingo_draws draw
    where draw.round_id = v_round.id
      and draw.ball_number = candidate
  )
  order by random()
  limit 1;

  if v_ball is null then raise exception 'no_numbers_left'; end if;

  select coalesce(max(draw_order), 0) + 1
    into v_order
  from public.bingo_draws
  where round_id = v_round.id;

  insert into public.bingo_draws (
    round_id, ball_number, draw_order, drawn_by
  ) values (
    v_round.id, v_ball, v_order, v_user_id
  );

  update public.bingo_rounds
  set status = 'playing', updated_at = now()
  where id = v_round.id;

  v_new_winners := public.nostra_bingo_register_winners(v_round.id, v_ball);

  if v_round.phase = 'full_card' and v_new_winners > 0 then
    update public.bingo_rounds
    set status = 'completed', updated_at = now()
    where id = v_round.id;
  end if;

  select coalesce(jsonb_agg(jsonb_build_object(
    'card_number', winner.card_number,
    'customer_name', winner.customer_name,
    'phase', winner.phase
  ) order by winner.card_number), '[]'::jsonb)
  into v_winners
  from public.bingo_winners winner
  where winner.round_id = v_round.id
    and winner.phase = v_round.phase
    and winner.trigger_ball = v_ball;

  return jsonb_build_object(
    'ball_number', v_ball,
    'draw_order', v_order,
    'phase', v_round.phase,
    'new_winners', v_winners
  );
end;
$$;

create or replace function public.clear_bingo_draws()
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_round_id bigint;
begin
  if not public.nostra_bingo_is_manager() then raise exception 'manager_required'; end if;

  select id into v_round_id
  from public.bingo_rounds
  where archived_at is null
  limit 1;

  if v_round_id is null then raise exception 'bingo_setup_missing'; end if;

  delete from public.bingo_winners where round_id = v_round_id;
  delete from public.bingo_draws where round_id = v_round_id;

  update public.bingo_rounds
  set phase = 'one_line', status = 'closed', updated_at = now()
  where id = v_round_id;
end;
$$;

create or replace function public.reset_bingo_round()
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_current public.bingo_rounds%rowtype;
  v_new_id bigint;
begin
  if not public.nostra_bingo_is_manager() then raise exception 'manager_required'; end if;

  select * into v_current
  from public.bingo_rounds
  where archived_at is null
  limit 1
  for update;

  if v_current.id is null then raise exception 'bingo_setup_missing'; end if;

  update public.bingo_rounds
  set status = 'archived', archived_at = now(), updated_at = now()
  where id = v_current.id;

  insert into public.bingo_rounds (title, card_price, status, phase)
  values (v_current.title, v_current.card_price, 'open', 'one_line')
  returning id into v_new_id;

  return v_new_id;
end;
$$;

